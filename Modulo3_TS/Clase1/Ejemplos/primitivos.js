"use strict";
//Ejemplo de tipos de primitivos en Typescript
//1.String
let nombre = "Fiorella Rodriguez";
let saludo = `Hola ${nombre}, bienvenida a typescript`;
console.log(saludo);
//2. Number
let entero = 42;
let decimal = 3.14;
let cientifico = 1e5;
console.log("Entero: ", entero);
console.log("Decimal: ", decimal);
console.log("Cientifico: ", cientifico);
//3. boolean
let esEstudiante = true;
let tieneLicencia = false;
console.log("Es estudiante? ", esEstudiante);
console.log("Tiene licencia? ", tieneLicencia);
//4. null
let valorNulo = null;
console.log("Valor nulo: ", valorNulo);
//5. undefined
let valorIndefinido = undefined;
console.log("Valor indefinido: ", valorIndefinido);
